package A_april.Operators;

public class Relational {
    public static void main(String[] args) {
        float a=.1f;
        float b=1.1f;
        System.out.println(b>a);
        System.out.println(a<=b);
        System.out.println(a!=b);
        System.out.println(b!=b);
        System.out.println(++a==b++);
        System.out.println(a);
        System.out.println(b);

    }
}
