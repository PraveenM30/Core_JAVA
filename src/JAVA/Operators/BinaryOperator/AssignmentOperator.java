package JAVA.Operators.BinaryOperator;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a=10;
        int b=20;

        a+=b;
        System.out.println(a);

        b-=a;
        System.out.println(b);

        a*=b;
        System.out.println(a);
    }
}
